# Funciones para imprimir por pantalla


def imprimir_libro(libro):
    """Función encargada de imprimir los atributos de un libro."""
    print(f"0. Autor: {libro[0]}")
    print(f"1. Título: {libro[1]}")
    print(f"2. Género: {libro[2]}")
    print(f"3. Editorial: {libro[4]}")
    print(f"4. Año de Publicación: {libro[5]}")
    print(f"5. Serie de Libros: {libro[6]}")
    print(f"6. Número de Páginas: {libro[7]}")
    print(f"7. Cantidad de Ejemplares: {libro[8]}")
    print(f"8. Disponibilidad:  {libro[9]}")
    print(f"9. Cantidad de libros disponibles: {libro[10]}")
